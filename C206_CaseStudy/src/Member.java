public class Member {
	private String memberName;
	private String memberCCA;
	
	public Member(String memberName, String memberCCA) {
		this.memberName = memberName;
		this.memberCCA = memberCCA;
	}
	
	public String getMemberName() {
		return memberName;
	}
	
	public String getMemberCCA() {
		return memberCCA;
	}
}
